import UIKit

extension UIFont {
    
    static func SFPro18() -> UIFont? {
        return UIFont.init(name: "SF Pro Display", size: 18)
    }
    
    static func SFPro20() -> UIFont? {
        return UIFont.init(name: "SF Pro Display", size: 20)
    }
    
    
    static func SFPro25() -> UIFont? {
        return UIFont.init(name: "SF Pro Display", size: 25)
    }
}
