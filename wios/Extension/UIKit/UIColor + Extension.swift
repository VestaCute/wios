import UIKit

extension UIColor {
    
    static func mainWhite() -> UIColor {
        return #colorLiteral(red: 0.9266447425, green: 0.9417319894, blue: 0.954197228, alpha: 1)
    }
    
    static func bottonColor() -> UIColor {
        return #colorLiteral(red: 0.8131790757, green: 0.8482470512, blue: 0.8861126304, alpha: 1)
    }
    
}
